// Function expression for divide
const divide = function(a = 2000, b = 100) {
    return a / b;
};

// Arrow function for square
const square = x => x * x;

// Arrow function for add
const add = (a, b) => a + b;

// Example usages (optional, just for testing)
console.log(divide()); // 20
console.log(square(2)); // 4
console.log(add(3, 4)); // 7